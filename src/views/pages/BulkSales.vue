            <h2>About Bulk Condo Sales</h2>
            <p>Bulk condo sales became popular during the last real estate correction in 2008 and are expected to accelerate over the next 36 months. It is a process by which real estate developers, banks, successor financial institutions,and other bulk buyers make blocks of units available to investors at deep discounts.&nbsp; Typically, a bulk buyer can acquire bulk units for 35% - 50% below retail pricing.</p>
            <p>When the real estate market slows, developers run the risk of defaulting on their construction loans which are paid down through unit sales in the building.&nbsp; In the last real estate cycle, this became a significant issue since end-user buyer deposit requirements were low - sometimes only 5% of the purchase price. With low deposits at risk, buyer&rsquo;s simply walked away from their contracts leaving the developer with large quantities of unsold unit inventory to service.</p>
            <p>In the most recent real estate development cycle and boom, buyer&rsquo;s are required to have closer to 50% of the purchase price in escrow at the time of contract, so the risk of buyer default is lower. Also, in the event of a default, the developer has significantly more equity in the units to support the continued paydown of their construction loans through the slower sales period.</p>
            <p>There are two primary types of bulk purchases: (1) bulk purchase of units from a developer in a new building; and (2) bulk purchase of units from a developer that has completed a condo conversion of an old rental building to a condo. The types of investors and issues vary across both types of transactions. For purposes of this guide and in the light of a pending global real estate correction, we are primarily focused on bulk sales of new construction units.</p>

            <h2>Why Would a Developer Make a Bulk Condo Sale?</h2>
            <p>Even with more unit equity in place through higher end-user deposits, there are still several reasons for developers to make bulk sales when the market starts to slow:</p>
            <ol class="condos-list-1">
                <li class="number-list-item"><strong>Profits</strong> - the primary reason for a developer to bulk sale condo units is to make money. Typically, the construction loan issued to a developer to construct the building is paid down through a release schedule. This means that a portion of the loan is paid off through every unit sale and the unit is &ldquo;released&rdquo; from the construction lender&rsquo;s blanket mortgage.&nbsp; This structure ensures that the lender receives the lion&rsquo;s share of payments from unit sales until their loan is paid in full, which may require the sale of 70-80% of the building.&nbsp; The last 20-30% of the units include the developer&rsquo;s profit in the project.&nbsp; In a slowing market, the developer may look to bulk sell the remaining unsold units in order to capture profits and return money to its investors - even when profits are lower than projected due to the bulk sale discount.</li>
                <li clas="number-list-item"><strong>Carrying Costs</strong> - Once the construction loan is paid off, the developer still needs to pay the costs to operate and &ldquo;carry&rdquo; the unsold unit inventory. These costs may include the applicable percentage of common area charges assessed by the homeowner&rsquo;s association and the on-going marketing and sales costs, which often include the management of an expensive sales center. A quick bulk sale of units would eliminate these carrying costs for the developer.</li>
                <li class="number-list-item"><strong>Management</strong> - Until the developer inventory is all sold (the exact percentage is based on the specific condominium documents and State law), the developer manages the homeowner&rsquo;s association and has the added responsibility to manage both their unsold units and the common areas of the building on behalf of the other unit buyers. Although third-party property manager&rsquo;s are put in place, it is still a hassle for the developer to remain part of the association as a multiple unit owner and may consider a bulk sale to eliminate the associated management responsibilities.</li>
                <li class="number-list-item"><strong>Loan Guarantee</strong> - In instances where the developer has not sold the requisite number of units to pay off the construction loan, they may be incentivized to sell a block of units in a bulk sale to satisfy the loan and limit any personal guarnatees put in place with the financing.</li>
            </ol>
            <h2>Why would a Lender Make a Bulk Condo Sale?</h2>
            <p>In certain instances, a financial institution or construction lender may end up taking units back from the developer which are typically pledged as collateral for the construction loan. &nbsp; Banks are not in the business of managing and selling individual condo units, so they will look to &ldquo;bulk sell&rdquo; the units to an investor.&nbsp; Also, during the last real estate down cycle, Federal banking regulators pressured banks to get rid of non-performing assets, leading to an increase in bulk condo sales for units they recaptured.</p>

            <h2>Who Determines the &ldquo;Bulk Condo Sales&rdquo; Pricing?</h2>
            <p>In a bulk condo sale, the developer and their lender will agree on a below market price they would accept to sell a block of units at a discount in exchange for a fast sale.&nbsp; The developer and their sales team would then offer the units in bulk to investors through a private sale or broader offering to the public.&nbsp;</p>

            <h2>How are Bulk Condo Sales Different from Auctions?</h2>
            <p>Bulk condo sales are similar to auctions only to the extent that a buyer can acquire the units at below market pricing. Unlike an auction where prices can be &ldquo;bid up&rdquo; (as much as 50%+) from their opening price by interested investors, bulk condo sales are made on a first-come, first-served basis and the block of units is sold at the offering price.&nbsp;</p>
            <h2>Who Makes Bulk Condo Purchases and Why?</h2>
            <p>Investors typically make bulk condo purchases from developer and lender sellers. The investors take on the responsibility of operating the units as low-yield rentals while the market corrects.&nbsp; Once prices come back, the investor can then resell the individual units at a big profit.&nbsp; Investors include individual domestic and international buyers looking for hard assets to appreciate following the market decline as well as hedge funds and other opportunistic investors.&nbsp;</p>
            <p>During times of increased competition for rental yields, the cap rates on traditional multi-family rental properties decrease.&nbsp; This has led institutional investors to seek opportunities afforded through bulk sales of &ldquo;fractured&rdquo; condo projects. Cap rates are typically higher than for comparable apartment properties due to less competition and potentially more complexity with a bulk condo sale.&nbsp; Due to the significant competition to buy rental properties and yield, the number of investors willing to consider fractured / bulk condo deals has been increasing. This has led to a decrease of cap rate spreads between bulk condo and apartment purchases.</p>
            <p>In 2019, there were approximately 30 bulk condo sales in Florida according to Luke Wickham, an institutional property broker with Marcus &amp; Millichap.&nbsp; The same quantity of bulk sales transactions was expected for 2020, prior to the onset of the Coronavirus global pandemic. The global market shock from the economic shut-down is expected to immediately slow end-user sales and drive a growing number of developer bulk sales to investors looking for rental yield and long-term appreciation.</p>
            <p>The added complexity to purchasing bulk condo units is related to fragmented ownership whereby the buyer of a bulk unit portfolio becomes a large part of the association and must work with individual unit owners in the building. Also, the bulk unit buyer often steps into the shoes of the developer and takes on associated liability.&nbsp;</p>
            <p>Eric Granowsky, a principal with ESG Kullen, one of the most active condo-to-apartment investors, made 23 bulk condo purchases during the past decade. Grankowsky said that bulk <a href="/">condos</a> are a legitimate way by which multi-family investors can still invest in hot markets and receive a discount.&nbsp; Grankowsky however typically buys bulk units in older buildings and then deconverts the building from a condo to a rental - a process known as a &ldquo;round trip&rdquo;, going from rental-to-condo-and back to rental.&nbsp; To terminate the condo association as part of the deconversion, State laws enacted in 2007 to help support the real estate recovery, had allowed as little as 80% ownership to terminate the association. New laws subsequently enacted to protect owners now require up to 95% ownership, making it more difficult to deconvert.</p>
            <p>Lenders typically shied away from financing this type of bulk purchase, but ESG&rsquo;s Granowsky says that if you can acquire 80-90% of the units, you can now finance the acquisition as inexpensively as a regular multi-family deal.</p>

            <h2>Who Benefits from Bulk Condo Sales?</h2>
            <ul class="condos-list-1">
            <li><strong><em>Developers</em></strong> - released from construction loan guarantees and access to potential unit and building profits</li>
            <li><strong><em>Financial Institutions</em></strong> - construction loans are paid off and no requirement to foreclose and manage the unit collateral</li>
            <li><strong><em>Investors</em></strong> - acquire deeply discounted assets for short-term rental yield and longer-term asset appreciation for resale</li>
            <li><strong><em>Renters</em></strong> - benefit from increased rentals on the market and associated decreases in rental rates in new buildings</li>
            </ul>
            <h2>Risks and Negative Impacts from Bulk Condo Sales?</h2>
            <ul class="condos-list-1">
            <li><em><strong>Investors</strong> - </em>if an investor buys a block of units in a bulk sale, and does not buy enough to control the condo association, there can be significant risks to the investment. For example, it is possible that the Board votes to prevent rentals at the property, eliminating the opportunity for short rental yield on the investment ahead of unit resales.</li>
            <li><strong><em>Institutional Apartment Owners</em></strong> - if a bulk buyer acquires a block of units for cash with the hope of long term appreciation and resale, they may undercut market rents in order to quickly fill the unit during the recovery period. This will lead to potential vacancies in rental buildings and a downward pressure on rents across institutional rental portfolios.</li>
            <li><strong><em>Unit Owners</em></strong> - In the event an investor acquires a block of units in a bulk condo sale, there is the potential for the investor to rent out the units leading to an increase of renters within the building and decreasing overall unit values for owners.</li>
            </ul>

            <h2>Market Conditions are Ripening for Bulk Sales</h2>
            <p>According to a comprehensive report completed for Manhattan, the largest condo market in the world, as of the 4th quarter 2019 almost 1 in 4 new units built since 2013 remained unsold, amounting to approximately 4,100 out of 16,200 units delivered. This number is conservative as it does not include data from thousands of units in under-construction buildings which suffer from the same market dynamics. All of this data will be further exacerbated by the negative impact on buyers post Coronavirus.</p>
            <p>The condo oversupply is leading to sales tactics reminiscent of the last market down cycle which included bulk sales to investors, buildings converting to rentals, rent-to-own options and super-sized commissions for agents that deliver luxury buyers.&nbsp; To disguise widespread discounting on units, developers often hide discounts by offering to pick-up transfer taxes, applicable luxury or mansion taxes and other costs, effectively resulting in discounts for the buyer.</p>
            <p>Bulk sales and purchases had been refused by most developers throughout&nbsp; 2019, but now they are beginning to talk, said Elliot Bogod of Broadway Realty, who represents a number of investors.&nbsp; The increasing use of rent-to-own programs, with developers applying up to 75% of the rent paid towards the purchase, has not been used since the last correction and is viewed by many as a bad sign for developers and an arbiter of things to come.</p>
            <p>Although the report focused exclusively on Manhattan, the over-supply and buyer dynamics is similar across other condo markets including Miami, Chicago, Boston and Los Angeles.&nbsp;</p>
            <p>The growing oversupply coupled with slowing sales will lead to accelerated desperation by developers and the willingness to entertain bulk condo sales.</p>

            <h2>Conclusion</h2>
            <p>Bulk condo sales, often referred to as &ldquo;fractured sales&rdquo; are typically seen in distressed markets when end-user sales have slowed or stalled.&nbsp; The process of selling multiple units in one transaction at a discount allows the developer or lender in possession of the units to sell fast, eliminating the inventory from their balance sheets and reducing carrying costs.&nbsp;</p>
            <p>Likewise, the buyer of bulk condo units has a unique opportunity to acquire units below market value and to rent the units for rental income with minimum yield while waiting for the market to correct and resell the units individually for a profit.&nbsp;</p>
            <p>In all instances, whether bulk sales are done in a new building or an older condo conversion property, there are both risks and rewards for institutional and individual investors. Risks are tied to unfavorable changes imposed by condo associations, a flood of rental properties on the market and potentially extended recovery periods. Rewards are the financial gains from short-term rental yields and profits on future unit resales.</p>
            <p>The most recent real estate boom was driven by new construction, as opposed to condo conversions that were a hallmark of the last cycle that ended in 2008. The growing over-supply of new construction units, developed during the past decade in the top cities around the world, is the result of low interest rates helping developers build and helping end-users finance and buy.</p>
