            <h2>About Buying a Condo Pre-Construction - The Process</h2>
            <p>Pre-construction <a href="/">condos</a> are condo units typically sold through a 4-step process prior to the developer completing construction of the building and delivering units to&nbsp; end-user buyers.</p>
            <p>Due to the requirement for <a href="./developer.html">developers</a> to &ldquo;pre-sell&rdquo; a percentage of the building (50-75%) in order to receive a construction loan, pre-construction sales have become and important sales channel for developers. Early buyers that complete a non-binding letter of intent / reservation or execute a binding contract, prior to&nbsp; the 2-3 year construction period commencing, are able to purchase units at a discount when the building is completed.</p>
            <p>During the last condo boom, which extended through 2007, developers made it easy to buy and flip reservations and contracts, creating an environment ripe for creative financing and fraud.&nbsp; With lessons learned, the most recent cycle of new construction condo development has more stringent deposit requirements and limits on contract assignment.</p>
            <p>Regardless of the new restrictions, pre-construction buying as a way to help developers secure financing and to provide financial incentives to buyers, is still very much alive and active around the world. The following guide provides details to help you navigate this growing market and potentially make money by buying &ldquo;pre-construction&rdquo;.</p>
            <h2>The 4 Steps of a Pre-Construction Sale</h2>
            <h3 class="guide-h3">#1 Reservation</h3>
            <p>Non-binding reservation agreement. 10% deposit. Developers are still finalizing plans with the City and State. Either party can void at any time before the contract and all deposits are returned.</p>
            <p><strong><em>Pros</em></strong> - non-binding, low and refundable deposit, best pricing, widest unit selection options</p>
            <p><strong><em>Cons</em></strong> - details may change, maybe never built, long time to delivery</p>
            <h3 class="guide-h3">#2 Contract</h3>
            <p>Once a developer gets an approved &ldquo;offering plan&rdquo; from local and state governments,&nbsp; the purchase contract and condominium documents are delivered to reservation holders (typically 3-6 months after reservation). Buyers have a 15 calendar day recission period after signing. Developers will require an additional 10%+ and full deposit is non-refundable.</p>
            <p><strong><em>Pros</em></strong> - details finalized, more certainty the project will get built; below market pricing; and wide unit selection</p>
            <p><strong><em>Cons</em></strong> - larger deposit (20%+), 15-days to cancel before binding and non-refundable</p>
            <h3 class="guide-h3">#3 Construction</h3>
            <p>Groundbreaking on foundation and then 24-36 months of construction. Additonal 10%+ deposit. Additional deposits during construction stages - ie. 25th floor or top-off. Typcially 50% total deposit before closing.&nbsp;</p>
            <p><strong><em>Pros</em></strong> - three years or less to closing and occupancy, below market prices</p>
            <p><strong><em>Cons</em></strong> - less selection, large non-refundable deposits</p>
            <h3 class="guide-h3">#4 Completion /Closing</h3>
            <p>Once the&nbsp; developer receives the certificate of occupancy (CO), developer sends out letters to request closing within 30-days.&nbsp; Final money exchanged and the buyer becomes the official owner.</p>
            <p><strong><em>Pros</em></strong> - see finished product before buying, fast closing, immediate occupancy, flexible payment terms, incentives on remaining inventory</p>
            <p><strong><em>Cons</em></strong> - close to market prices, limited inventory, if any</p>
            <h2>Advantages to Pre-Construction Buying</h2>
            <p>The top advantage to buying pre-construction is cost savings. By buying early, developers can show progress to their lenders and have built-in pricing flexibility. The early discount pricing is known in the industry as Schedule A pricing, typically the lowest pricing available to the public.</p>
            <p>The secondary benefit to pre-construction buying is the unit availability. The earlier you commit in the process, the bigger the selection of units available to you,</p>
            <h2>Risks to Pre-Construction Buying</h2>
            <p>To balance the potential financial reward of buying during the pre-construction phase, there are several risks of which you should be aware.</p>
            <ul>
            <li><strong>Performance and Delivery</strong> - Since pre-construction buyers typically have to wait 2-3 years for the building to be completed, they run the risk of non-performance by the developer. &nbsp; This makes it critical to understand the developer and their reputation. A large developer with a good reputation has a lot to lose and is more likely to perform all of their obligations in a timely manner as compared to a small or one-off developers that may not perform.</li>
            <li><strong>Increased Deposits</strong> - The biggest cost to the a pre-construction buyer, independent of the purchase price when closing on the unit, is the deposit. A resale transaction typically requires a 10% deposit ahead of closing, whereas new construction deposits can range for 20-50%. These deposit amounts are negotiable however, especially in a slowing market where developers are more willing to negotiate.</li>
            <li><strong>Construction Delays</strong> - Unlike buying a completed resale unit, a pre-construction unit is subject to construction delays. Buyers should therefore have some flexibility built into their expected delivery and occupancy dates.&nbsp; To protect against extensive delays, it is important to negotiate an &ldquo;outside date&rdquo; so that you have the right to cancel the contract and get your deposit back if the outside delivery data is not met. This is particularly helpful in the event of a market correction, allowing you to re-invest your deposit into another market or project.</li>
            <li><strong>Allowed Variances</strong> - Condominium documents filed with the State typically allow for 5% +/- variances for things including square footage and ceiling heights. Be prepared for developers to deliver the finished product with 5% less than what was advertised.</li>
            <li><strong>Commercial Unit Owners</strong> - Oftentimes a developer will retain a certain number of ground floor units for a restaurant or retail space, known as the commercial condo units. In order to provide disproportionate value to these commercial units retained by the developer / sponsor, the condo documents may provide the commercial units with a maximum amount of common area charges or a limit to annual assessments. Both of these things would result in disproportionate monthly expenses paid by resident unit owners.&nbsp; Be careful when buying pre-construction units in a mixed-use project.</li>
            <li><strong>Unexpected Notice to Close</strong> - The contract for a new construction unit typically provides the developer with the right to provide a 30-day notice to close once they&rsquo;re received their TCO or CO - temporary or final certificate of occupancy. If the unit is an investment that was acquired 2-3 years prior, you may not be ready to close and should look to negotiate a 10-15 day extension in the event you are out of the country or need to liquidate other assets to close.<br /><br /></li>
            </ul>
            <h2>Pre-construction Closing Costs</h2>
            <p>Typically, the seller is responsible for paying transfer taxes and their own legal fees in a resale transaction. However, many developers / sponsors of new construction projects expect their fees to also be paid by the buyer. In a softening market, the buyer can negotiate these fees as well as the payment by the developer of luxury &amp; mansion taxes, where applicable.</p>
            <h3 class="guide-h3">Here is a list of some of the typical buyer closing fees associated with a pre-construction transaction:</h3>
            <p><strong>Development Fee</strong> - a % of the purchase price and additional charges for options not included in purchase price. Typically 1.5% - 2.7%</p>
            <p><strong>Capital contribution</strong> - A working capital contribution equal to 2 months of HOA fees payable to the association to provide it with initial funding. Can be used by HOA for any purpose and is not credited towards other assessments and charges</p>
            <p><strong>Reimbursements to seller / developer</strong> -</p>
            <p>(i) utility / telecom reimbursement for deposits or fees advanced by the developer for the benefit of the specific unit being purchased. Amount is not known until closing and is typically de minimis</p>
            <p>(ii) reimbursement for closing fees - not known until closing and is typically de minimis</p>
            <p><strong>Outstanding amounts</strong> - amounts for sales tax for options and upgrades</p>
            <p><strong>Late funding fees</strong><em> -</em> fees charged to the buyer for late funding of deposits or other payment obligations</p>
            <p><strong>Attorney fees</strong><em> - </em>legal fees are negotiable items, but typically in a resale transaction, each party pays for their own legal. In pre-construction, the developer can try to get the Buyer to pay for their legal fees associated with the unit closing.</p>
            <p><strong>Buyer&rsquo;s title insurance, title search and title review</strong><em> - </em>negotiable&nbsp; and optional if Seller elects top pay</p>
            <h3 class="guide-h3">Here is a list of some of the typical seller / developer closing fees associated with a pre-construction transaction</h3>
            <p><strong>Deed recording in public records</strong> - a de minimis transaction fee</p>
            <p><strong>Documentary stamp taxes</strong> = equal to $.60 per $100 in purchase price</p>
            <p><strong>Title insurance premium</strong> - if issued by Seller&rsquo;s closing agent</p>
            
            <h2>The Condominium Documents</h2>
            <p>The &ldquo;Condo Docs&rdquo; include all of the information related to buying and living in the condominium. It is critical that you and your legal counsel read the entire set of documents to avoid any issues at closing.</p>
            <p><strong>The documents include several ownership and operating details including:</strong></p>
            <ul>
            <li>the Brand Agreement if the building is a branded building, rights relating to management of the building</li>
            <li>leasing of developer-owned units</li>
            <li>management details</li>
            <li>apportionment of common area expenses</li>
            <li>closing expenses</li>
            <li>detailed unit floor plans</li>
            <li>survey and plot plans</li>
            <li>rights to lease your unit</li>
            <li>by-laws relating to the Association</li>
            <li>operating budget of the association</li>
            </ul>

            <p><strong>There are several items to be aware of before finalizing your contract:</strong></p>

            <ol>
            <li><strong>Square footage</strong> - since you are buying based on square footage, it is critical for you to understand what is included in the square footage of your unit. Typically, square footage is the entirety of the interior space. This includes columns and interior walls which may reduce the liveable square footage. The condo documents will break out the Unit Area (liveable) from the Typical Area (what is being paid for). Rely on your broker or legal counsel to make sure you know exactly what and how much you are buying.</li>
            <li><strong>Closing Expenses</strong> - as shared above, buyer&rsquo;s will typically have to pay the developer an additional 1.75% of the purchase price at closing for the &ldquo;Developer Fee&rdquo;, plus 2x the monthly association dues which go towards reserves for the building.</li>
            <li><strong>Lease Terms</strong> - it is important to understand your rights related to leasing your unit if you are purchasing as an investment. Developers often limit leases to 2x per year and no leases for less than 30-days - essentially limiting Airbnb and other short-term lease options.</li>
            </ol>
            <li><strong>Time for Closing</strong> - The condo documents will also set forth the time for closing when construction is completed. Make sure you are able to close at that time to avoid litigation around your deposit.</li>
